public class Store {
// Store 클래스는 1번 필수 요구 사항

    private Menu menu;
    private Item item;

    // getter() , setter()
    public Menu getMenu() {
        return menu;
    }

    public Item getItem() {
        return item;
    }

    public void setMenu(Menu menu) {
        this.menu = menu;
    }

    public void setItem(Item item) {
        this.item = item;
    }


}
